uint32_t g_task_find(const char* name, int maxTries) {

	uint32_t id = -1;
	int tries = 0;

	while (id == -1 && tries < maxTries) {
		id = g_task_get_id(name);
		++tries;

		if (id == -1) {
			// TODO limit tries
			g_sleep(100);
		}
	}

	if (id == -1) {
		// TODO set errno
	}

	return id;
}
